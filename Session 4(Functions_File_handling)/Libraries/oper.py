# Python Module operations
def add(a, b):
    """
    This program adds two
    numbers and return the result
    """
    result = a + b
    return result


def sub(a, b):
    """
    This program adds two
    numbers and return the result
    """
    result = a - b
    return result


def mul(a, b):
    """
    This program adds two
    numbers and return the result
    """
    result = a * b
    return result


def mod(a, b):
    """
    This program adds two
    numbers and return the result
    """
    result = a % b
    return result